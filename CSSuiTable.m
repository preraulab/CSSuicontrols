                'if(cmd.cmd==="setData"){' ...
                'var tbody=document.querySelector("tbody");' ...
                'if(!tbody)return;' ...
                'tbody.innerHTML="";' ...
                'cmd.data.forEach(function(row,ri){' ...
                'var tr=document.createElement("tr");' ...
                'tr.className="tbl-row";' ...
                'tr.setAttribute("data-row",ri+1);' ...
                'row.forEach(function(cell,ci){' ...
                'var td=document.createElement("td");' ...
                'td.setAttribute("data-col",ci+1);' ...
                'td.textContent=cell;' ...
                'tr.appendChild(td);' ...
                '});' ...
                'tbody.appendChild(tr);' ...
                '});' ...
                '_attachRowListeners(tbody);' ...
                '}' ...
                'if(cmd.cmd==="setSelection"){' ...
                'var sel=cmd.selection||[];' ...
                'if(_selType==="row"){' ...
                'document.querySelectorAll(".tbl-row").forEach(function(r){' ...
                'var ri=parseInt(r.getAttribute("data-row"));' ...
                'if(sel.indexOf(ri)>=0)r.classList.add("selected");' ...
                'else r.classList.remove("selected");' ...
                '});' ...
                '}' ...
                '}' ...
                '};' ...
                '</script>' ...
            ];

            html = [ ...
                '<!DOCTYPE html><html><head><style>' css '</style></head><body>' ...
                '<div id="css-root">' ...
                '<div class="css-control css-surface">' ...
                '<table><thead><tr>' hdrHTML '</tr></thead>' ...
                '<tbody>' bodyHTML '</tbody></table>' ...
                '</div>' ...
                '</div>' ...
                compJS '</body></html>' ...
            ];
        end

        function onMessage(obj, data)
            switch data.event
                case 'ready'
                    if ~isempty(obj.Selection_)
                        obj.pushCmd(struct('cmd','setSelection', ...
                            'selection', obj.Selection_));
                    end
                case 'change'
                    if obj.Enabled_
                        oldSel = obj.Selection_;
                        raw    = data.selection;
                        if isempty(raw) || (isstruct(raw) && isempty(fieldnames(raw)))
                            newSel = [];
                        elseif strcmp(obj.SelectionType,'row')
                            if isstruct(raw)
                                newSel = cell2mat(struct2cell(raw))';
                            elseif iscell(raw)
                                newSel = cell2mat(raw);
                            else
                                newSel = raw;
                            end
                            newSel = newSel(:)';
                        else
                            % cell mode: list of [r,c] pairs
                            if iscell(raw)
                                newSel = cell2mat(cellfun(@(x)x(:)', raw, 'UniformOutput',false));
                            elseif isstruct(raw)
                                newSel = [];
                                fn = fieldnames(raw);
                                for k = 1:numel(fn)
                                    pair = raw.(fn{k});
                                    if isstruct(pair)
                                        pv = cell2mat(struct2cell(pair))';
                                    else
                                        pv = pair(:)';
                                    end
                                    newSel = [newSel; pv]; %#ok<AGROW>
                                end
                            else
                                newSel = raw;
                            end
                        end
                        obj.Selection_ = newSel;
                        if ~isempty(obj.SelectionChangedFcn)
                            evt = struct('Source', obj, ...
                                'Selection', newSel, ...
                                'PreviousSelection', oldSel);
                            try
                                obj.SelectionChangedFcn(obj, evt);
                            catch ME
                                warning('CSSuiTable:callbackError','%s',ME.message);
                            end
                        end
                    end
                case 'dblclick'
                    if obj.Enabled_ && ~isempty(obj.DoubleClickFcn)
                        evt = struct('Source', obj, 'Row', data.row);
                        try
                            obj.DoubleClickFcn(obj, evt);
                        catch ME
                            warning('CSSuiTable:dblclickError','%s',ME.message);
                        end
                    end
            end
        end

    end

    % =====================================================================
    methods (Static, Access = private)

        function out = cellToJSArray(c)
            %cellToJSArray  Convert N×M cell to cell-of-rows (cell of cell of char).
            % This is the format expected by the JS setData handler.
            if isempty(c)
                out = {};
                return;
            end
            [nr, nc] = size(c);
            out = cell(nr, 1);
            for r = 1:nr
                row = cell(1, nc);
                for col = 1:nc
                    v = c{r,col};
                    if isnumeric(v), v = num2str(v); end
                    row{col} = char(v);
                end
                out{r} = row;
            end
        end

    end

