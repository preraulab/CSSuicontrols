        function onMessage(obj, data)
            switch data.event
                case 'ready'
                    if ~isempty(obj.Data_)
                        obj.pushDataToJS();
                    end
                    if ~isempty(obj.Filter_)
                        obj.pushCmd(struct('cmd','setFilter','value',obj.Filter_));
                    end
                case 'search'
                    obj.Filter_ = data.value;
                    obj.fireCallback(obj.SearchSubmittedFcn, ...
                        struct('Source',obj,'Value',data.value));
                case 'nodeClick'
                    obj.fireCallback(obj.NodeClickedFcn, ...
                        struct('Source',obj,'NodeData',data.data,'Text',data.text));
                case 'nodeDoubleClick'
                    obj.fireCallback(obj.NodeDoubleClickedFcn, ...
                        struct('Source',obj,'NodeData',data.data,'Text',data.text));
                case 'menuAction'
                    obj.fireCallback(obj.MenuActionFcn, ...
                        struct('Source',obj,'Action',data.action, ...
                               'NodeData',data.data,'Text',data.text));
            end
        end

        function fireCallback(obj, fn, evt)
            if isempty(fn), return, end
            try
                if iscell(fn)
                    f = fn{1}; f(obj, evt, fn{2:end});
                else
                    fn(obj, evt);
                end
            catch ME
                warning('CSSuiTree:callback','%s', ME.message);
            end
        end
    end
