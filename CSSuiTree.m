classdef CSSuiTree < CSSBase
    %CSSUITREE  Filterable hierarchical tree backed entirely by HTML/JS.
    %
    %   Purpose:
    %       Native uitree gets sluggish past a few thousand nodes because
    %       each TreeNode is a MATLAB UI primitive. This control renders
    %       the tree as DOM <li> nodes once, then filters by toggling
    %       display:none — interactive on 10k+ nodes.
    %
    %   Usage:
    %       t = CSSuiTree(parent, 'Style','shadow_light');
    %       t.Data = struct('text','Root','data','/path', ...
    %                       'children', { ... nested ... });
    %       t.NodeClickedFcn       = @(s,e) disp(e.NodeData);
    %       t.NodeDoubleClickedFcn = @(s,e) edit(e.NodeData);
    %
    %   Data schema (JSON-encodeable nested struct or cell):
    %       node = struct('text','...', 'data','...', ...
    %                     'isLeaf', tf, 'children', {{ ... }});
    %       'data'     : arbitrary string the JS bridge round-trips back
    %                    via the click event (typically a filesystem path).
    %       'isLeaf'   : optional. Auto-detected from empty children if not set.
    %       'children' : cell array of nested node structs.
    %       Provide a single root node or a cell array of top-level nodes.
    %
    %   Name-Value Pairs:
    %       'Data'                  : struct or cell of structs (default empty)
    %       'ShowSearchBar'         : logical (default true)
    %       'SearchPlaceholder'     : char (default 'Filter…')
    %       'AccentColor'           : char (default '#296ec7')
    %       'ExpandOnFilter'        : logical (default true) — expand all
    %                                 ancestor branches when filter is non-empty
    %       'NodeClickedFcn'        : @(s,e) on single click of a leaf
    %       'NodeDoubleClickedFcn'  : @(s,e) on double-click of a leaf
    %       'SearchSubmittedFcn'    : @(s,e) when query is committed
    %
    %   Event struct fields (NodeClickedFcn / NodeDoubleClickedFcn):
    %       evt.Source   = obj
    %       evt.NodeData = the 'data' field of the clicked node
    %       evt.Text     = the visible label
    %
    %   ∿∿∿  Prerau Laboratory MATLAB Codebase · sleepEEG.org  ∿∿∿

    properties (Access = public)
        ShowSearchBar         = true
        SearchPlaceholder     = 'Filter…'
        AccentColor           = '#296ec7'
        ExpandOnFilter        = true
        NodeClickedFcn        = []
        NodeDoubleClickedFcn  = []
        SearchSubmittedFcn    = []
        MenuActionFcn         = []   % @(src,evt) on context-menu item click;
                                     % evt.Action / .NodeData / .Text
    end

    properties (Dependent)
        Data
        Filter
    end

    properties (Access = protected)
        Data_   = []
        Filter_ = ''
    end

