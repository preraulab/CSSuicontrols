        end

        function v = get.Filter(obj), v = obj.Filter_; end
        function set.Filter(obj, val)
            obj.Filter_ = char(val);
            if ~obj.Updating_ && obj.Loaded_
                obj.pushCmd(struct('cmd','setFilter','value',obj.Filter_));
            end
        end

        function set.SearchPlaceholder(obj, val)
            obj.SearchPlaceholder = val;
            if ~obj.Updating_ && obj.isReady(), obj.refresh(); end
        end
        function set.ShowSearchBar(obj, val)
            obj.ShowSearchBar = val;
            if ~obj.Updating_ && obj.isReady(), obj.refresh(); end
        end
